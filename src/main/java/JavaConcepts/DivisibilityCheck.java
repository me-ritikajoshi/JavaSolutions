package JavaConcepts;

public class DivisibilityCheck {
    public static void main(String[] args) {
        int num=3;
        if(num%3==0 && num%5==0){
            System.out.println("Divisible by 3 and 5");
        }else{
            System.out.println("Not Divisible by 3 and 5");

        }
    }

}
