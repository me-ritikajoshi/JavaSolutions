package JavaContent;

public class NumberSystems {
    public static void main(String[] args) {
        /*
        decimals are given as usual numbers
        binary is given by 0b
        octal is given by 0
        and hexadecimal are given by 0x
        */
        int dec = 152;
        int bin = 0b10011;
        int oct = 0230;
        int hex = 0x1234;

        System.out.println("In decimal: " +dec);
        System.out.println("In binary: " +bin);
        System.out.println("In octal: " +oct);
        System.out.println("In hexadecimal: " +hex);

    }
}
